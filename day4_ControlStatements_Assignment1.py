#Write a Python program that takes a number as input and prints "Even" if the number is even and "Odd" if it's odd.
num=int(input("Enter the number: "))# taking input from user
if num%2==0:                 #if condition to check if num divided by 2 gives remainder 0 or not.
    print("This number is even") #if above condition is true print this
else:
    print("This number is odd") #if above condition is false print this
              
